    private void Insertar() throws IOException{
      String id = txtid.getText(), nombre=txtnombre.getText(), apellido=txtapellido.getText();
      int telefono = Integer.parseInt(txttelefono.getText());
      
        if (id!=""&&nombre!=""&&apellido!=""&&telefono!=0) {  
            
         FileWriter fichero = new FileWriter("datos.txt",true);
         
         PrintWriter guardar = new PrintWriter(fichero);
         
         guardar.println(""+id+"\t"+nombre+"\t"+apellido+"\t"+telefono+"");
         guardar.close();
         
         JOptionPane.showMessageDialog(null, "Datos almacenados correctamente");
         Limpiar();
         
        }else{
        JOptionPane.showMessageDialog(null, "No se ingresaron los datos correctamente");
        Limpiar();
        }
    }
    private void Buscar() throws FileNotFoundException{
        Limpiar();
        String codb = JOptionPane.showInputDialog("Ingrese codigo a buscar");
        String cod,nom,ape;
        int tele;
        int ban = 1;
        
        FileReader fr = new FileReader("datos.txt");
        Scanner inFile = new Scanner(fr);
        
        do{
            cod= inFile.next();
            nom = inFile.next();
            ape = inFile.next();
            tele = inFile.nextInt();
                if (codb.equals(cod)) {
                JOptionPane.showMessageDialog(null, "Datos encontrados con exito");
                txtid.setText(cod);
                txtnombre.setText(nom);
                txtapellido.setText(ape);
                txttelefono.setText(""+tele);
                ban=0;
                }else if (inFile.hasNext()==false) {
                  JOptionPane.showMessageDialog(null, "Los datos no se encuentran en la base de datos intente otra vez");
                  ban=0;
            }
        }while(ban==1);
        
        inFile.close();
        
    }
